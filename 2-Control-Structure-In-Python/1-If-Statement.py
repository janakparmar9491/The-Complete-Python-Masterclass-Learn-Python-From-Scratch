age = int(input('Enter your age'))

if age >= 18:
    print("You are an adult.")
else:
    print("You are not an adult")


marks = int(input('Enter your marks'))

if marks >= 90:
    print("Grade A")
elif marks >= 70:
    print("Grade B")
elif marks >= 60:
    print("Grade C")
elif marks < 60:
    print("Grade D")

